# a divisão será um FLOAT
print(10 / 2)
# divisão FLOAT
print(type(10 / 2))

print(10 / 3)

# embora a divisão seja INTEIRA, o resultado pode ser um FLOAT
print(10 // 3)
# divisão INTEIRA
print(type(10 // 3))

# embora a divisão seja INTEIRA, o resultado será um FLOAT 
print(10 // 3.3)
# a divisão é INTEIRA, mas o resultado é FLOAT
print(type(10 // 3.3))
print(10 / 2.5)

